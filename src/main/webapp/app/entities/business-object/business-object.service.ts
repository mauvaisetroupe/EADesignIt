import axios from 'axios';

import { type IBusinessObject } from '@/shared/model/business-object.model';

const baseApiUrl = 'api/business-objects';
const basePlantUMLApiUrl = 'api/plantuml/business-object/get-svg';

export default class BusinessObjectService {
  public getPlantUML(id: number): string | PromiseLike<string> {
    return new Promise<string>((resolve, reject) => {
      axios
        .get(`${basePlantUMLApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public find(id: number): Promise<IBusinessObject> {
    return new Promise<IBusinessObject>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IBusinessObject): Promise<IBusinessObject> {
    return new Promise<IBusinessObject>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IBusinessObject): Promise<IBusinessObject> {
    return new Promise<IBusinessObject>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IBusinessObject): Promise<IBusinessObject> {
    return new Promise<IBusinessObject>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
